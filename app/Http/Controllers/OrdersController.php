    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $order = Order::find($id);
        $order->delete();
        // Check if user is admin
        if (Auth::guard('admin')->check()) {
            return redirect('/orders')->with('success', 'Order canceled');
        }
        // If it's regular user return to books page
        return redirect('/inventory')->with('success', 'Order canceled');
    }
}
